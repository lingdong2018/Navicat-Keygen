#奥运五环
import turtle

turtle.width(10)
turtle.color('Black')
turtle.circle(100)
turtle.penup()
turtle.goto(230,0)
turtle.pendown()

turtle.color('red')
turtle.circle(100)
turtle.penup()
turtle.goto(-230,0)
turtle.pendown()

turtle.color('blue')
turtle.circle(100)
turtle.penup()
turtle.goto(-100,-100)
turtle.pendown()

turtle.color('yellow')
turtle.circle(100)
turtle.penup()
turtle.goto(100,-100)
turtle.pendown()

turtle.color('green')
turtle.circle(100)
